int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	dif;

	dif = 0;
	i = 0;
	while (s1[i] != '\0')
	{
		while (s1[i] == s2[i])
		{
			if (s1[i] == '\0')
			{
				return (0);
			}
			i++;
		}
		if (s1[i] != s2[i])
		{
			dif = s1[i] - s2[i];
			return (dif);
		}
	}
	return (0);
}
