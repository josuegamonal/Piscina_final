void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	i;
	int	c;

	i = 0;
	c = size -1;
	while (i < c)
	{
		temp = tab[i];
		tab[i] = tab[c];
		tab[c] = temp;
		i++;
		c--;
	}
}
